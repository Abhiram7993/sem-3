public class operators{

    public static void main(String[] args){

        int a = 10, b = 5;

        // arithmatic operation
        System.out.println("a+b = "+ (a+b));

        // relational operator
        System.out.println("a>b: "+ (a>b));

        // logical operator
        System.out.println("(a>b)||(b>a): "+ ((a>b)||(b>a)));

    }
}